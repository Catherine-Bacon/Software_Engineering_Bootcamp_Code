grade = int(input("Enter your grade: "))
if grade >= 80:
    print("Congratulations! You have an A")
elif grade >= 70:
    print("Good job! You have a B")
elif grade >= 60:
    print("Keep it up! You have a C")
elif grade >= 50:
    print("Try a little harder next time! You have a D")
else:
    print("Oh No! You have an F")
